/**
 * NAC npm package build.
 *
 * Single source of truth: ../../yujin.app/nac-spec/js/*.js. We do
 * NOT copy/maintain a duplicate in this package. The build reads
 * those files and produces ESM + CJS + browser + d.ts wrappers in
 * dist/.
 *
 * Run: node ./build.mjs
 */

import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const SRC_DIR  = path.resolve(__dirname, '..', '..', 'yujin.app', 'nac-spec', 'js');
const DIST_DIR = path.resolve(__dirname, 'dist');

const ENTRIES = [
  { name: 'nac',                src: 'nac.js',                global: 'NAC'        },
  { name: 'nac-v2-extensions',  src: 'nac-v2-extensions.js',  global: 'NACv2'      },
  { name: 'nac-chat-client',    src: 'nac-chat-client.js',    global: 'NacChat'    }
];

function ensureDir(p) {
  fs.mkdirSync(p, { recursive: true });
}
function read(p) {
  return fs.readFileSync(p, 'utf8');
}
function write(p, content) {
  ensureDir(path.dirname(p));
  fs.writeFileSync(p, content);
}

function buildEsm(name, code) {
  // The reference runtime attaches itself to window.<global>. In ESM
  // we install a synthetic window/globalThis target, run the IIFE,
  // and re-export the global as the module default.
  return `// Generated by build.mjs -- do not edit. Source: yujin.app/nac-spec/js/${name}.js
const _g = typeof globalThis !== 'undefined' ? globalThis : (typeof window !== 'undefined' ? window : {});
${code}
export default _g.NAC || _g.NACv2 || _g.NacChat || _g[Object.keys(_g).find(k => /^NA[Cc]/.test(k))];
export const NAC      = _g.NAC;
export const NACv2    = _g.NACv2;
export const NacChat  = _g.NacChat;
`;
}

function buildCjs(name, code) {
  return `// Generated by build.mjs -- do not edit. Source: yujin.app/nac-spec/js/${name}.js
const _g = typeof globalThis !== 'undefined' ? globalThis : (typeof window !== 'undefined' ? window : {});
${code}
module.exports = _g.NAC || _g.NACv2 || _g.NacChat;
module.exports.NAC      = _g.NAC;
module.exports.NACv2    = _g.NACv2;
module.exports.NacChat  = _g.NacChat;
`;
}

function buildBrowser(name, code) {
  return `// Generated by build.mjs -- do not edit. Source: yujin.app/nac-spec/js/${name}.js
${code}
`;
}

